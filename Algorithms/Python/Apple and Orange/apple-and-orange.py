def countApplesAndOranges(s, t, a, b, apples, oranges):
    ap = 0
    ora = 0
    for i in apples:
        c = i+a
        if c in range(s,t+1):
            ap += 1
    for j in oranges:
        c = j+b
        if c in range(s,t+1):
            ora += 1
    print(ap)
    print(ora)

